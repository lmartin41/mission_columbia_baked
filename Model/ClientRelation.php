    public function fixAge($data) {
        if ($this->data['Client']['age'] == null) {
            return true;
        }
        $year = Date('Y') - $this->data['Client']['age'];
        $this->data['Client']['DOB'] = $year . "-01-01 00:00:00";
        return true;
